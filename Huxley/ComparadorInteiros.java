import java.util.Scanner;

public class ComparadorInteiros {
	public static void main(String[] args) {
		Scanner tec = new Scanner(System.in);

		int x = tec.nextInt();
		int y = tec.nextInt();

		if (true) {
			if (x > y) {
				System.out.println("1");
			}else {
				System.out.println("0");
			}
		}if (true) {
			if (x == y) {
				System.out.println("1");
			}else {
				System.out.println("0");
			}
		}if (true) {
			if (x < y) {
				System.out.println("1");
			}else {
				System.out.println("0");
			}
		}if (true) {
			if (x != y) {
				System.out.println("1");
			}else {
				System.out.println("0");
			}
		}if (true) {
			if (x >= y) {
				System.out.println("1");
			}else {
				System.out.println("0");
			}
		}if (true) {
			if (x <= y) {
				System.out.println("1");
			}else {
				System.out.println("0");
			}
		}
	}
}